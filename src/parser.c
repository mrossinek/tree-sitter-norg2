      END_STATE();
    case 18:
      ACCEPT_TOKEN(sym_space);
      if (lookahead == 11) ADVANCE(19);
      if (lookahead == '\t' ||
          lookahead == ' ') ADVANCE(18);
      END_STATE();
    case 19:
      ACCEPT_TOKEN(sym_space);
      if (lookahead == '\t' ||
          lookahead == 11 ||
          lookahead == ' ') ADVANCE(19);
      END_STATE();
    case 20:
      ACCEPT_TOKEN(sym_punctuation);
      END_STATE();
    case 21:
      ACCEPT_TOKEN(sym_line_break);
      END_STATE();
    case 22:
      ACCEPT_TOKEN(anon_sym_STAR);
      END_STATE();
    case 23:
      ACCEPT_TOKEN(anon_sym_STAR);
      if (lookahead == ' ') ADVANCE(25);
      if (lookahead == '*') ADVANCE(4);
      END_STATE();
    case 24:
      ACCEPT_TOKEN(anon_sym_PLUS);
